package Question6;

public abstract class Employee {
String name;
int ssn;
String address;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getSsn() {
	return ssn;
}
public void setSsn(int ssn) {
	this.ssn = ssn;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}

}
