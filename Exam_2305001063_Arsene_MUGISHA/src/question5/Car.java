public class Car {
	
	String make;
	int yearModel;
	
	public String getMake() {
		return make;
	}
	public void setMake(String make) {
		this.make = make;
	}
	public int getYearModel() {
		return yearModel;
	}
	public void setYearModel(int yearModel) {
		this.yearModel = yearModel;
	}
	public String toString() {
		return "Car [make=" + make + ", yearModel=" + yearModel + "]";
	}
	
	

}
